package com.cryptenet.thanatos.pop;

public class POP {
    public static void main(String[] args) {
        int radius = 5;

        System.out.println("Circle Perimeter: " + 2 * 3.1416 * radius);
        System.out.println("Circle Area: " + 3.1416 * radius * radius);

        int width = 5;
        int height = 10;

        System.out.println("Rectangular Perimeter: " + 2 * (width + height));
        System.out.println("Rectangular Area: " + width * height);
    }
}
